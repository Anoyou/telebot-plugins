def _pick_next_idiom(last_char: str, used: set[str]) -> str | None:
    candidates = IDIOM_DB.get(last_char, [])
    available = [c for c in candidates if c not in used]
    if not available:
        return None
    return random.choice(available)


def _validate_answer(answer: str, last_char: str, used: set[str], forbidden_words: list[str], used_forbidden: set[str]) -> tuple[bool, str]:
    if len(answer) != 4:
        return False, "必须是四个字"
    if answer[0] != last_char:
        return False, f"要以「{last_char}」开头"
    if answer in used:
        return False, "这个成语已经用过了"
    # 禁词检查
    for fw in forbidden_words:
        if fw in answer and fw in used_forbidden:
            return False, f"禁词「{fw}」本轮已经出现过了"
    return True, ""


# ─────────────────────────────────────────────────────
# 插件
# ─────────────────────────────────────────────────────
@register
class IdiomChainPlugin(Plugin):
    key = "idiom_chain"
    display_name = "成语接龙"
    message_channels = {"incoming", "outgoing"}
    owner_only = False
    command_config_keys = {"command", "timeout", "forbidden_words"}

    def __init__(self) -> None:
        super().__init__()
        self._command = "cy"
        self._timeout = 120
        self._forbidden_words: list[str] = []
        self._games: dict[int, ChainGame] = {}
        self._locks: dict[int, asyncio.Lock] = {}
        self._tasks: set[asyncio.Task] = set()

    def _get_lock(self, chat_id: int) -> asyncio.Lock:
        if chat_id not in self._locks:
            self._locks[chat_id] = asyncio.Lock()
        return self._locks[chat_id]

    def _track_task(self, task: asyncio.Task) -> None:
        self._tasks.add(task)
        task.add_done_callback(self._tasks.discard)

    async def on_startup(self, ctx: PluginContext) -> None:
        cfg = ctx.config or {}
        self._command = cfg.get("command", "cy")
        self._timeout = cfg.get("timeout", 120)
        self._forbidden_words = cfg.get("forbidden_words", [])
        self.commands = {self._command: self._cmd_handler}
        if ctx.log:
            await ctx.log("info", f"[idiom_chain] 已启动，指令：{self._command}，禁词：{self._forbidden_words}")

    async def on_shutdown(self, ctx: PluginContext) -> None:
        for task in list(self._tasks):
            task.cancel()
        if self._tasks:
            await asyncio.gather(*self._tasks, return_exceptions=True)
        self._tasks.clear()
        self._games.clear()
        self._locks.clear()
        if ctx.log:
            await ctx.log("info", "[idiom_chain] 已停止")

    async def on_interaction(
        self,
        ctx: PluginContext,
        entry_key: str,
        payload: dict[str, Any],
    ) -> list[dict[str, Any]] | None:
        if entry_key != "start_idiom_chain":
            return None
        timeout = int(payload.get("timeout") or self._timeout or 120)
        return [{"type": "send_message", "text": f"📚 成语接龙入口已触发，限时 {timeout} 秒。发送 ,{self._command} 100 开局。"}]

    def _format_game_prompt(self, game: ChainGame) -> str:
        lines = [
            f"<b>📜 成语接龙</b> 第 {game.round_num} 轮",
            f"",
            f"当前成语：<b>{game.current_idiom}</b>",
            f"请接「<b>{game.current_idiom[-1]}</b>」开头的成语",
            f"奖励：+{game.prize}",
        ]
        if self._forbidden_words:
            remaining = [fw for fw in self._forbidden_words if fw not in game.used_forbidden]
            if remaining:
                lines.append(f"禁词：{'、'.join(remaining)}（每个只能用一次）")
        lines.append(f"限时 {self._timeout} 秒，直接发成语即可")
        return "\n".join(lines)

    async def _cmd_handler(
        self, client: Any, event: Any, args: list[str], account_id: int, ctx: PluginContext,
    ) -> None:
        chat_id = int(getattr(event.chat_id, "channel_id", None) or event.chat_id or 0)
        if not chat_id:
            return

        arg_str = " ".join(args).strip().lower()
        if arg_str in ("stop", "结束", "停止"):
            async with self._get_lock(chat_id):
                if chat_id in self._games:
                    self._games.pop(chat_id)
                    await event.reply("📜 成语接龙已结束", parse_mode="html")
            return

        prize = self._parse_prize(args)
        if prize <= 0:
            await event.reply(f"请指定奖励金额，例如：,{self._command} 100", parse_mode="html")
            return

        # 开始新游戏
        async with self._get_lock(chat_id):
            if chat_id in self._games and self._games[chat_id].waiting:
                await event.reply("已有进行中的接龙~", parse_mode="html")
                return

            starter = random.choice(random.choice(list(IDIOM_DB.values())))
            game = ChainGame(
                current_idiom=starter,
                used_idioms={starter},
                round_num=1,
                prize=prize,
                started_at=time.monotonic(),
            )
            self._games[chat_id] = game

        msg = await event.reply(self._format_game_prompt(game), parse_mode="html")
        game.message_id = int(getattr(msg, "id", 0) or 0) or None
        self._track_task(asyncio.create_task(self._auto_timeout(chat_id, ctx, game.started_at)))

    async def on_message(self, ctx: PluginContext, event: Any) -> None:
        text = (getattr(event, "raw_text", "") or "").strip()
        if len(text) != 4:
            return
        if text.startswith(",") or text.startswith("/"):
            return

        chat_id = int(getattr(event.chat_id, "channel_id", None) or event.chat_id or 0)
        if not chat_id:
            return

        game = self._games.get(chat_id)
        if not game or not game.waiting:
            return

        lock = self._get_lock(chat_id)
        async with lock:
            game = self._games.get(chat_id)
            if not game or not game.waiting:
                return

            last_char = game.current_idiom[-1]
            ok, reason = _validate_answer(text, last_char, game.used_idioms, self._forbidden_words, game.used_forbidden)
            if not ok:
                await event.reply(f"❌ {reason}", parse_mode="html")
                return

            sender = await event.get_sender()
            name = getattr(sender, "first_name", "") or "玩家"
            player_id = int(getattr(sender, "id", 0) or 0)

            game.used_idioms.add(text)
            # 检查是否命中禁词
            for fw in self._forbidden_words:
                if fw in text:
                    game.used_forbidden.add(fw)

            game.round_num += 1
            game.winner_id = player_id
            game.winner_name = name
            message_id = int(getattr(event, "id", 0) or 0) or None
            await self._send_prize_reply(ctx, event, chat_id, message_id, game.prize)

            # 自动生成下一题
            next_char = text[-1]
            next_idiom = _pick_next_idiom(next_char, game.used_idioms)
            if next_idiom:
                game.current_idiom = next_idiom
                game.used_idioms.add(next_idiom)
                game.started_at = time.monotonic()
                started_at = game.started_at
                await self._edit_game_message(
                    ctx,
                    chat_id,
                    game,
                    f"\n\n✅ {name} 答对「{text}」，奖励 <b>+{game.prize}</b>\n\n{self._format_game_prompt(game)}",
                )
                self._track_task(asyncio.create_task(self._auto_timeout(chat_id, ctx, started_at)))
            else:
                # 接不下去了，游戏结束
                game.waiting = False
                await self._edit_game_message(
                    ctx,
                    chat_id,
                    game,
                    f"\n\n✅ {name} 答对「{text}」，奖励 <b>+{game.prize}</b>\n🏆 接龙结束！以「{text[-1]}」开头的成语都用完了，共 {game.round_num} 轮",
                )
                self._games.pop(chat_id, None)

    @staticmethod
    def _parse_prize(args: list[str]) -> int:
        if not args:
            return 0
        try:
            return max(0, min(1_000_000, int(args[0])))
        except ValueError:
            return 0

    async def _send_prize_reply(self, ctx: PluginContext, event: Any, chat_id: int, message_id: int | None, prize: int) -> None:
        text = f"+{prize}"
        try:
            await event.reply(text)
            return
        except Exception:
            pass
        if ctx.client and message_id:
            try:
                await ctx.client.send_message(chat_id, text, reply_to=message_id)
                return
            except Exception:
                pass
        if ctx.client:
            await ctx.client.send_message(chat_id, text)

    async def _edit_game_message(self, ctx: PluginContext, chat_id: int, game: ChainGame, text: str) -> None:
        if not ctx.client or not game.message_id:
            return
        try:
            await ctx.client.edit_message(chat_id, game.message_id, text, parse_mode="html")
        except Exception as exc:
            if ctx.log:
                await ctx.log("warn", f"[idiom_chain] 题目消息更新失败：{type(exc).__name__}: {exc}")

    async def _auto_timeout(self, chat_id: int, ctx: PluginContext, started_at: float) -> None:
        await asyncio.sleep(self._timeout)
        async with self._get_lock(chat_id):
            game = self._games.get(chat_id)
            if not game or not game.waiting or game.started_at != started_at:
                return
            game.waiting = False
            last_char = game.current_idiom[-1]
            answer = _pick_next_idiom(last_char, game.used_idioms)
            self._games.pop(chat_id, None)
        if ctx.log:
            await ctx.log("info", f"[idiom_chain] chat {chat_id} 超时，答案是 {answer}")


